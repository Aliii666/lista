lista = [1,2,3,4]
print(lista[0])
print(lista[-1])
print(lista[-2])
print(lista[-3])

#append - agregar un valor a la lista
lista.append(5)
#insert - inserta un elemento especifico y lugar especifico
lista.insert(1,10)
#remove - Elimina un elemento especifico
lista.remove(4)
#pop - Elimina un indice especifico  INDICE NUMERO DE LA POSICION 
lista.pop(0)
#clear
lista.clear()





print(lista)